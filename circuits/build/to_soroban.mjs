import { readFileSync, writeFileSync } from "fs";
const vk = JSON.parse(readFileSync("verification_key.json"));
const proof = JSON.parse(readFileSync("proof.json"));
const pub = JSON.parse(readFileSync("public.json"));

const be32 = (dec) => BigInt(dec).toString(16).padStart(64, "0");
const G1 = (p) => be32(p[0]) + be32(p[1]);              // X‖Y
const G2 = (p) => be32(p[0][1]) + be32(p[0][0]) + be32(p[1][1]) + be32(p[1][0]); // c1‖c0 (imaginary-first, EVM convention)

let out = "// AUTO-GENERATED by circuits/build/to_soroban.mjs — snarkjs → Soroban BN254 fixture.\n";
out += "// Do not edit by hand; regenerate with the circuit build.\n\n";
out += `pub const VK_ALPHA: &str = "${G1(vk.vk_alpha_1)}";\n`;
out += `pub const VK_BETA: &str = "${G2(vk.vk_beta_2)}";\n`;
out += `pub const VK_GAMMA: &str = "${G2(vk.vk_gamma_2)}";\n`;
out += `pub const VK_DELTA: &str = "${G2(vk.vk_delta_2)}";\n`;
out += `pub const VK_IC: [&str; ${vk.IC.length}] = [\n`;
for (const p of vk.IC) out += `    "${G1(p)}",\n`;
out += "];\n\n";
out += `pub const PROOF_A: &str = "${G1(proof.pi_a)}";\n`;
out += `pub const PROOF_B: &str = "${G2(proof.pi_b)}";\n`;
out += `pub const PROOF_C: &str = "${G1(proof.pi_c)}";\n`;
out += `pub const PUB_SIGNALS: [&str; ${pub.length}] = [\n`;
for (const s of pub) out += `    "${be32(s)}",\n`;
out += "];\n";
writeFileSync("../../programs/olio-pool/src/fixture.rs", out);
console.log("wrote fixture.rs: IC.len =", vk.IC.length, "pub.len =", pub.length);

// Also emit the VK as JSON for `stellar contract invoke ... set_verifier_key`.
const vkJson = {
  alpha: G1(vk.vk_alpha_1),
  beta: G2(vk.vk_beta_2),
  gamma: G2(vk.vk_gamma_2),
  delta: G2(vk.vk_delta_2),
  ic: vk.IC.map(G1)
};
writeFileSync("vk_soroban.json", JSON.stringify(vkJson));
console.log("wrote vk_soroban.json");
